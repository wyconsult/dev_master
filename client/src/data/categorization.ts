// Função para sugerir categoria automaticamente baseada no objeto da licitação
export function suggestCategory(objeto: string): string {
  const objetoLower = objeto.toLowerCase();
  
  // Verificar alimentação
  const alimentacaoKeywords = [
    'alimentação', 'refeição', 'merenda', 'cozinha', 'cozinheiro', 'merendeira', 
    'nutrição', 'restaurante', 'coffee', 'lanche', 'buffet', 'copeiragem'
  ];
  
  if (alimentacaoKeywords.some(keyword => objetoLower.includes(keyword))) {
    return 'alimentacao';
  }
  
  // Verificar limpeza
  const limpezaKeywords = [
    'limpeza', 'limpador', 'faxina', 'higienização', 'conservação'
  ];
  
  if (limpezaKeywords.some(keyword => objetoLower.includes(keyword))) {
    return 'limpeza';
  }
  
  // Verificar sites/portais
  const sitesKeywords = [
    'www.', 'http', '.com', '.gov', '.org', '.br', 'portal', 'site', 'compras'
  ];
  
  if (sitesKeywords.some(keyword => objetoLower.includes(keyword))) {
    return 'sites';
  }
  
  return 'outros';
}